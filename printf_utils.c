#include "libft/libft.h"

char 	*ft_string_to_char(char *s)
{
	char *aux;

	if (!(aux = malloc(2 * sizeof(char))))
				return (0);
	*aux = *s;
	*(aux + 1 ) = '\0';
	return (aux);
}
